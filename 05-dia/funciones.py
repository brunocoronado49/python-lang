def greetings():
    '''
    Es un saludo simple que se
    imprime en la consola
    '''
    print('Hello World!')
    
greetings()

def greetings_name(nombre):
    '''
    Es un saludo simple que se
    imprime en la consola con un nombre
    que recibe como parametro
    '''
    print(f'Hello {nombre}!')
    
greetings_name('Francisco')

def cuadrado_numero(numero):
    '''
    Imprime en la consola el cuadrado
    de un numero que se le manda como
    parametro
    '''
    print(numero ** 2)

cuadrado_numero(72)

