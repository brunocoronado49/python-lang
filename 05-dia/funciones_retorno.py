def multiplicar(num1, num2):
    return num1 * num2

print(multiplicar(5, 200))

def potenciar_numero(numero, potenciador):
    return numero ** potenciador

print(potenciar_numero(3, 4))

def invertir_palabra(palabra):
    return ''.join(reversed(palabra.upper()))

print(invertir_palabra('Python'))

